<template>
  <div class="flex items-center">
    <div class="w-1/2">
      <v-select
        v-model="example.model"
        no-filter
        item-text="title"
        item-value="pageid"
        :items="example.items"
        :state="example.state"
        :inline="false"
        :is-loading="example.isLoading"
        style-menu="shadow"
        @input:value="debouncedQuery($event)"
        autocomplete
      ></v-select>
    </div>
    <div class="ml-10">
      <span class="font-semibold">v-model:</span>
      {{ example.model }}
    </div>
  </div>

  <v-card
    v-if="current"
    base="flat-card"
    style-card="rounded"
    class="p-4 mt-6"
  >
    <ul>
      <li>
        <span class="font-semibold">
          {{ current.title }}
        </span>
      </li>
      <li class="my-4">
        <a
          :href="`https://en.wikipedia.org/?curid=${current.pageid}`"
          class="link"
        >
          {{ `https://en.wikipedia.org/?curid=${current.pageid}` }}
        </a>
      </li>
    </ul>
    <iframe v-if="current" :src="`https://en.wikipedia.org/?curid=${current.pageid}`" frameborder="0" class="w-full h-[600px]"></iframe>
  </v-card>
</template>

<script>
import { ref, reactive, computed } from "vue";
import { debounce } from "../../tools.js";

export default {
  components: {},
  setup() {
    let example = reactive({
      model: [],
      items: [],
    });

    const urlWiki =
      "https://en.wikipedia.org/w/api.php?action=query&format=json&list=prefixsearch&formatversion=2&origin=*&pssearch=";

    let current = computed(() => {
      return example.items.find((i) => i.pageid === example.model);
    });

    let query = (q) => {
      if (q === "") return example.items;

      example.isLoading = true;

      fetch(`${urlWiki}${q}`)
        .then((response) => response.json())
        .then((data) => {
          example.items = data.query.prefixsearch;
        })
        .finally(() => {
          example.isLoading = false;
        });
    };

    let debouncedQuery = debounce(query, 300);

    return {
      example,
      query,
      debounce,
      debouncedQuery,
      current,
    };
  },
};
</script>
